package com.java;

public class LOOPS {
	
	public static void main(String[] args) {
		
		//FOR LOOPS
		for (int i = 1 ; i <= 10 ; i++ ) {
		System.out.println(i);
	}
		//REVERSE
		for (int a = 10 ; a >= 0 ; a-- ) {
		System.out.println(a);
	}
		//WHILE LOOP
		int j = 5;
		while (j >= 0) {
			System.out.println(j);
			j = j-1;
		}
		//DO WHILE
		int k = 10;
		do {
			System.out.println(k);
			k = k - 1;
		}
		while (k >= 1);
		}
		
	}

